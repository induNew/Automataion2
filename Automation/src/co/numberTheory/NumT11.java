package co.numberTheory;

import java.util.Scanner;

public class NumT11 {

	public static void main(String[] args) {
		
		
		for(int i=0;i<100;i++)
		{
			 if(i%4==0 && i%5==0)
			{
				System.out.println("lara");
				
			}
		
			 else if(i%4==0)
			{
				System.out.println("java");
			}
			else if(i%5==0)
			{
				System.out.println("j2ee");
			}
			
		
			else
			{
				System.out.println("");
			}
		}
	}

}
