package co.numberTheory;

import java.util.Scanner;

public class NumT8 {

public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	System.out.println("Enter number to have its five multiples : ");
	int f=sc.nextInt();
	int k=0;
	for(int i=1;i<5;i++)
	{
		k=f*i;
		System.out.println(k);
	}
	
	
	
	
	
	
}

}
